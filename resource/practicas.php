<script>
	function ver_mas_practicas(el){
		$(".btn-"+el+"-open").fadeOut('slow');
		$('.'+el+' img').fadeOut('slow',function(){
			$('.'+el+' p').slideDown('slow',function(){
				$('.btn-'+el+'-close').fadeIn('slow');
			});

		});
	}

	function ver_menos_practicas(el){
		$('.btn-'+el+'-close').fadeOut('slow');
		$('.'+el+' p').slideUp('slow',function(){
			$('.'+el+' img').fadeIn('slow', function() {
				$(".btn-"+el+"-open").fadeIn('slow');
			});
		});
	}
</script>